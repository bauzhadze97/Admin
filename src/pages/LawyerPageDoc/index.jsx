const LawyerPageDoc = () => {
  document.title = "  | Gorgia LLC";

  const ContractDisplay = {
    title: " ",
    locationAndDate: ".  --/---/-----.",
    parties: [
      "   / ------------ (/:---------------) :_____ :--------------",
      "    \" ( 14.07.2008 .  245621288  : .   . 10),    (/ 61003000277)."
    ],
    subject: "        : 2.1. ------------------",
    confidentiality: "       ...",
    priceAndPayment: "     ...",
    termAndTermination: "            ...",
    rightsAndDuties: " : )      -2   ...",
    liabilityAndGuarantees: "           ...",
    forceMajeure: "               ...",
    disputeResolution: "                ...",
    finalProvisions: "    /          ..."
  };

  const generatePDF = () => {
    const doc = new jsPDF();
    
    // Add custom font to jsPDF
    doc.addFileToVFS("NotoSansGeorgian-Regular.ttf", NotoSansGeorgianRegular);
    doc.addFont("NotoSansGeorgian-Regular.ttf", "NotoSansGeorgian", "normal");
    
    // Set the custom font
    doc.setFont("NotoSansGeorgian", "normal");
    doc.setFontSize(14);
    
    // Title
    doc.text(ContractDisplay.title, 10, 20);

    // Location and Date
    doc.setFontSize(12);
    doc.text(ContractDisplay.locationAndDate, 10, 30);
    
    let y = 40; // Initial Y position for text

    // Helper function to add sections with possible highlights
    const addSection = (title, content, highlightedText = []) => {
      // Add section title
      doc.setFontSize(14);
      doc.text(title, 10, y);
      y += 10;

      // Add content
      doc.setFontSize(12);
      const lines = doc.splitTextToSize(content, 180); // Line wrapping
      lines.forEach(line => {
        if (highlightedText.some(highlight => line.includes(highlight))) {
          // Highlighted text
          doc.setTextColor(255, 255, 0); // Yellow
          doc.setFillColor(0, 0, 0); // Black text color
          doc.rect(10, y - 6, 190, 8, "F"); // Highlight background
        }
        doc.text(line, 10, y);
        doc.setTextColor(0, 0, 0); // Reset to default color
        y += 10;

        if (y > 280) { // Check page overflow
          doc.addPage();
          y = 20; // Reset Y for new page
        }
      });
    };

    // Add sections
    addSection(" 1. ", ContractDisplay.parties.join("\n"));
    addSection(" 2.  ", ContractDisplay.subject, ["  "]);
    addSection(" 3. ", ContractDisplay.confidentiality);
    addSection(" 4.     ", ContractDisplay.priceAndPayment);
    addSection(" 5.      ", ContractDisplay.termAndTermination);
    addSection(" 6.    ", ContractDisplay.rightsAndDuties);
    addSection(" 7.    ", ContractDisplay.liabilityAndGuarantees);
    addSection(" 8.  ", ContractDisplay.forceMajeure);
    addSection(" 9.  ", ContractDisplay.disputeResolution);
    addSection(" 10.  ", ContractDisplay.finalProvisions);

    // Save the generated PDF
    doc.save("contract.pdf");
  };

  return (
    <React.Fragment>
      <div className="page-content">
        <Container fluid={true}>
          <Breadcrumbs
            title=""
            breadcrumbItem=" "
          />

          <Row>
            <Col xl={12}>
              <Card>
                <CardBody>
                  <h4 className="card-title mb-4">{ContractDisplay.title}</h4>
                  <p>{ContractDisplay.locationAndDate}</p>

                  <h5 className="mt-4"> 1. </h5>
                  {ContractDisplay.parties.map((party, index) => (
                    <p key={index}>{party}</p>
                  ))}

                  <h5 className="mt-4"> 2.  </h5>
                  <p>{ContractDisplay.subject}</p>

                  <h5 className="mt-4"> 3. </h5>
                  <p>{ContractDisplay.confidentiality}</p>

                  <h5 className="mt-4"> 4.     </h5>
                  <p>{ContractDisplay.priceAndPayment}</p>

                  <h5 className="mt-4"> 5.      </h5>
                  <p>{ContractDisplay.termAndTermination}</p>

                  <h5 className="mt-4"> 6.    </h5>
                  <p>{ContractDisplay.rightsAndDuties}</p>

                  <h5 className="mt-4"> 7.    </h5>
                  <p>{ContractDisplay.liabilityAndGuarantees}</p>

                  <h5 className="mt-4"> 8.  </h5>
                  <p>{ContractDisplay.forceMajeure}</p>

                  <h5 className="mt-4"> 9.  </h5>
                  <p>{ContractDisplay.disputeResolution}</p>

                  <h5 className="mt-4"> 10.  </h5>
                  <p>{ContractDisplay.finalProvisions}</p>

                  <Button color="primary" onClick={generatePDF}>
                    Download as PDF
                  </Button>
                </CardBody>
              </Card>
            </Col>
          </Row>
        </Container>
      </div>
    </React.Fragment>
  );
};

export default LawyerPageDoc;
